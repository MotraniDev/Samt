    private static AdhkarCollection Build(
        AdhkarCollectionKind kind,
        string titleKey,
        AdhkarLibraryGroup group,
        string icon,
        bool schedulable,
        IReadOnlyList<AdhkarItem> items)
        => new()
        {
            Kind = kind,
            TitleKey = titleKey,
            Group = group,
            IconHint = icon,
            IsSchedulable = schedulable,
            Items = items,
            SourceKey = "Adhkar.Source.AzkarMe"
        };

    private static AdhkarItem Item(
        string id,
        string arabic,
        int repeat = 1,
        string? benefit = null,
        string? reference = null,
        string? translationKey = null,
        bool isQuranicSurah = false,
        bool isAyatAlKursi = false)
        => new()
        {
            Id = id,
            ArabicText = arabic,
            RepeatCount = Math.Max(1, repeat),
            BenefitText = benefit,
            Reference = reference,
            TranslationKey = translationKey,
            IsQuranicSurah = isQuranicSurah,
            IsAyatAlKursi = isAyatAlKursi
        };

